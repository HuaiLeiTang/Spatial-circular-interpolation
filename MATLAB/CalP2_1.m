function p2 = CalP2_1(p1,p3)
% 根据起始、终止点计算出中间点

p2(1)=(p1(1)+p3(1))/2;
p2(3)=(p1(3)+p3(3))/2;

%p1、p3的距离
d1 = sqrt((p3(1)-p1(1)).^2+(p3(2)-p1(2)).^2+(p3(3)-p1(3)).^2);

if d1<=0.5
    h = d1/12;
elseif d1<=1
    h = d1/20;
elseif d1<=1.5
    h = d1/25;
else
    h = d1/30;
end

p2(2)=(p1(2)+p3(2))/2 + h;

end